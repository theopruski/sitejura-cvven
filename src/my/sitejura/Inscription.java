    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                // Envoyer
        try{
            if (txtnom.getText().isEmpty() || txtprenom.getText().isEmpty() || txtmail.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Champs invalide"); 
            }else{
            conbdd con = new conbdd();
            pst=con.con.prepareStatement("insert into inscription(nom,prenom,email,evenement_ID) values (?,?,?,?)");
            pst.setString(1, txtnom.getText());
            pst.setString(2, txtprenom.getText());
            pst.setString(3, txtmail.getText());
            int evenement_ID = 0;
            if (rs.next()) {
                evenement_ID = rs.getInt(1);
            } 
            pst.setInt(4, listevent.getSelectedIndex());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Inscrption enregistre");
            TableInscription();
            pst.close();
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(this, "Echec"+e);
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

