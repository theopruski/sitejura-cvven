    public void TableEvent(){
        String[] connecter = {"ID","Intitule","Theme","Date debut","Date fin","Participant max","Description","Organisateur","Salle"};
        String[] afficher = new String[10];
        DefaultTableModel model = new DefaultTableModel(null, connecter);
        try{
            conbdd con = new conbdd();
            st=con.con.createStatement();
            rs=st.executeQuery("select * from evenement");
            while (rs.next()){
                afficher[0] = rs.getString("ID");
                afficher[1] = rs.getString("intitule");
                afficher[2] = rs.getString("theme");
                afficher[3] = rs.getString("date_debut");
                afficher[4] = rs.getString("date_fin");
                afficher[5] = rs.getString("nb_part_max");
                afficher[6] = rs.getString("description");
                afficher[7] = rs.getString("organisateur");
                afficher[8] = rs.getString("salle");
                model.addRow(afficher);
            }
            tableevent.setModel(model);
        }catch (Exception e){
            JOptionPane.showMessageDialog(this, "Erreur d'exportation"+e);
            
        }
    }
    
    public void TableInscription(){
        String[] connecter = {"ID","Nom","Prenom","Email","Evenement","Theme","Date debut","Date fin","Participant max","Description","Organisateur","Salle"};
        String[] afficher = new String[12];
        DefaultTableModel model = new DefaultTableModel(null, connecter);
        try{
            conbdd con = new conbdd();
            st=con.con.createStatement();
            rs=st.executeQuery("select inscription.inscription_ID, inscription.nom, inscription.prenom, inscription.email, evenement.intitule, evenement.theme, evenement.date_debut, evenement.date_fin, evenement.nb_part_max, evenement.description, evenement.organisateur, evenement.salle " +
                     "from inscription " +
                     "inner join evenement ON inscription.evenement_ID = evenement.ID");
            while (rs.next()){
                afficher[0] = rs.getString("inscription_ID");
                afficher[1] = rs.getString("nom");
                afficher[2] = rs.getString("prenom");
                afficher[3] = rs.getString("email");
                afficher[4] = rs.getString("intitule");
                afficher[5] = rs.getString("theme");
                afficher[6] = rs.getString("date_debut");
                afficher[7] = rs.getString("date_fin");
                afficher[8] = rs.getString("nb_part_max");
                afficher[9] = rs.getString("description");
                afficher[10] = rs.getString("organisateur");
                afficher[11] = rs.getString("salle");
                model.addRow(afficher);
            }
            tableins.setModel(model);
        }catch (Exception e){
            JOptionPane.showMessageDialog(this, "Erreur d'exportation"+e);
            
        }
    }
    
